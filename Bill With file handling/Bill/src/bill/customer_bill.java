    private void txtdelKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtdelKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            String r = txtdel.getText();
            int del=Integer.parseInt(r);
            
            
            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel)jTable1.getModel();
            int l=jTable1.getRowCount();
            if(del<=0 || del>=l)
            {
                JOptionPane.showMessageDialog(this, "PLEASE ENTER ROW NUMBER BETWEEN 1 - "+(l+1));
                txtdel.setText("");
            }
            else
            {
                model.removeRow(del-1);
                int sum=0;
                for(int i=0;i<jTable1.getRowCount();i++)
                {
                    sum=sum + Integer.parseInt(jTable1.getValueAt(i,4).toString());
                }
                txtdel.setText("");
                txttotal.setText(Integer.toString(sum));
            }
        }
        
        
        
        
    }//GEN-LAST:event_txtdelKeyPressed
