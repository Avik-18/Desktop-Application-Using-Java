    private void txtpcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpcodeKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            FileReader fin = null;
            try {
                String id,na,p;
                int price,f=0;
                String pcode = txtpcode.getText();
                pcode=pcode.toUpperCase();
                fin = new FileReader("item.txt");
                try (BufferedReader ffin = new BufferedReader(fin)) {
                    while((id=ffin.readLine())!=null)
                    {
                        if(id.equalsIgnoreCase(pcode))
                        {
                            f=1;
                            na=ffin.readLine();
                            price=Integer.parseInt(ffin.readLine());
                            
                            
                            txtpname.setText(na);
                            p=Integer.toString(price);
                            txtprice.setText(p);
                        }
                    }
                    if(f==0)
                    {
                    JOptionPane.showMessageDialog(this, "Product Code Not Found");
                    }
                }
                fin.close();                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(customer_bill.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(customer_bill.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    fin.close();
                } catch (IOException ex) {
                    Logger.getLogger(customer_bill.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
                
                
                
            
            
            
        }
        
        
        
        
        
    }//GEN-LAST:event_txtpcodeKeyPressed

